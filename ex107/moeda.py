def metade(met):
    met /= 2
    return met


def dobro(dob):
    dob *= 2
    return dob


def aumento(aument):
    dez_por_cento = 0.1 * aument
    aument = dez_por_cento + aument
    return aument


def dimunuir(diminu):
    treze_por_cento = 0.13 * diminu
    diminu = diminu - treze_por_cento
    return diminu
